package unearth.storage;

public class Idxd<T> {

    private final int index;

    private final T t;

    public Idxd(int index, T t) {
        this.index = index;
        this.t = t;
    }

    public int getIndex() {
        return index;
    }

    public T getT() {
        return t;
    }
}
